// Helper functions to filter and sort resources
export const getResourcesByCategory = (categoryName: string) => {
  return resources.filter((resource) => resource.category === categoryName);
};

export const getCategoryById = (categoryId: string) => {
  return categories.find((category) => category.id === categoryId);
};

export const getCategoryByName = (categoryName: string) => {
  return categories.find((category) => category.name === categoryName);
};

export const getNewestResources = (limit = 6) => {
  return [...resources]
    .sort(
      (a, b) =>
        new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime()
    )
    .slice(0, limit);
};

export const getFeaturedCategories = (limit = 4) => {
  return categories.slice(0, limit);
};

export const searchResources = (query: string) => {
  const lowercaseQuery = query.toLowerCase();
  return resources.filter(
    (resource) =>
      resource.name.toLowerCase().includes(lowercaseQuery) ||
      resource.description.toLowerCase().includes(lowercaseQuery) ||
      resource.category.toLowerCase().includes(lowercaseQuery)
  );
};

export const sortResources = (resources: Resource[], sortBy: string) => {
  const sortedResources = [...resources];

  switch (sortBy) {
    case "nameAsc":
      return sortedResources.sort((a, b) => a.name.localeCompare(b.name));
    case "nameDesc":
      return sortedResources.sort((a, b) => b.name.localeCompare(a.name));
    case "newest":
      return sortedResources.sort(
        (a, b) =>
          new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime()
      );
    case "oldest":
      return sortedResources.sort(
        (a, b) =>
          new Date(a.createdAt).getTime() - new Date(b.createdAt).getTime()
      );
    default:
      return sortedResources;
  }
};
